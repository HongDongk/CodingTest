// 유사배열
// 밑에 morse는 배열이다!! 유사배열 따라서 morse[a]로 불러야 값을 얻을수있다!

function solution(letter) {
    morse = { 
    '.-':'a','-...':'b','-.-.':'c','-..':'d','.':'e','..-.':'f',
    '--.':'g','....':'h','..':'i','.---':'j','-.-':'k','.-..':'l',
    '--':'m','-.':'n','---':'o','.--.':'p','--.-':'q','.-.':'r',
    '...':'s','-':'t','..-':'u','...-':'v','.--':'w','-..-':'x',
    '-.--':'y','--..':'z'
    };

    return letter.split(' ').map((a) => morse[a]).join('');
}